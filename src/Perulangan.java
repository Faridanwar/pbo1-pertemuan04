public class Perulangan {
    public static void main(String [] args) {
        int gawitugas = 0;
        int tuntung = 5;

        System.out.println("tugas yang sudah ada : " + tuntung + " buting");
        System.out.println("tugas yang sudah tuntung : " + gawitugas + " buting" );

        for (gawitugas = 1 ; gawitugas <= tuntung ; gawitugas++){
            System.out.println("menggawi tugas..");
            System.out.println("tugas nang sudah digawi : " +gawitugas+ " buting" );
        }
    }
}


